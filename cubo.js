class Cubo{
    constructor( ladoA, ladoB, ladoC){
        this.ladoA = ladoA;
        this.ladoB = ladoB;
        this.ladoC = ladoC;
    }
    volumen(){
        return this.ladoA * this.ladoB * this.ladoC;
    }
    limitacion(){
        if (this.ladoA.charAt(),this.ladoB.charAt(),this.ladoC.charAt()) {
            return 1;
        }
        else if(this.ladoA.charAt(),this.ladoB.charAt(),this.ladoC.charAt()){
            return 1;
        }
    }
}

module.exports = {
    Cubo
}
